import SwiftUI

/// App spacing and layout constants
struct AppSpacing {
    // MARK: - Padding/Margin Sizes

    /// 4pt - Extra small spacing
    static let xs: CGFloat = 4

    /// 8pt - Small spacing
    static let sm: CGFloat = 8

    /// 12pt - Medium-small spacing
    static let md: CGFloat = 12

    /// 16pt - Medium spacing
    static let lg: CGFloat = 16

    /// 20pt - Large spacing
    static let xl: CGFloat = 20

    /// 24pt - Extra large spacing
    static let xxl: CGFloat = 24

    /// 32pt - Double extra large spacing
    static let xxxl: CGFloat = 32

    // MARK: - Corner Radius

    /// 4pt - Small border radius
    static let radiusSm: CGFloat = 4

    /// 8pt - Standard border radius
    static let radiusMd: CGFloat = 8

    /// 12pt - Large border radius
    static let radiusLg: CGFloat = 12

    /// 24pt - Extra large border radius (xl2 in design)
    static let radiusXl: CGFloat = 24

    // MARK: - Common Sizes

    /// Standard input field height
    static let inputHeight: CGFloat = 48

    /// Standard button height
    static let buttonHeight: CGFloat = 44

    /// Standard icon size
    static let iconSize: CGFloat = 16

    /// Status bar height
    static let statusBarHeight: CGFloat = 44

    /// Home indicator height
    static let homeIndicatorHeight: CGFloat = 34

    // MARK: - Content Widths

    /// Standard mobile width
    static let mobileWidth: CGFloat = 375

    /// Standard mobile height
    static let mobileHeight: CGFloat = 812
}
